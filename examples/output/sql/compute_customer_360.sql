/* Customer 360 aggregation from Oracle ERP */
SELECT
  c.CUSTOMER_ID,
  c.FIRST_NAME + ' ' + c.LAST_NAME AS FULL_NAME,
  COALESCE(c.EMAIL, 'unknown@company.com') AS EMAIL,
  CASE WHEN NOT c.PHONE IS NULL THEN c.PHONE ELSE 'N/A' END AS PHONE,
  CASE
    WHEN c.STATUS = 'A'
    THEN 'Active'
    WHEN c.STATUS = 'I'
    THEN 'Inactive'
    WHEN c.STATUS = 'S'
    THEN 'Suspended'
    ELSE 'Unknown'
  END AS STATUS_LABEL,
  CAST(c.CREATED_DATE AS VARCHAR(MAX)) AS CREATED_DATE_STR,
  STR_TO_DATE(c.LAST_LOGIN, '%d-%b-%Y') AS LAST_LOGIN_DATE,
  GETDATE() AS SNAPSHOT_DATE,
  COALESCE(o.ORDER_COUNT, 0) AS TOTAL_ORDERS,
  COALESCE(o.TOTAL_REVENUE, 0) AS LIFETIME_REVENUE,
  COALESCE(w.SESSION_COUNT, 0) AS WEB_SESSIONS,
  STRING_AGG(o.PRODUCT_CATEGORY, ', ') WITHIN GROUP (ORDER BY
    CASE WHEN o.PRODUCT_CATEGORY IS NULL THEN 1 ELSE 0 END, o.PRODUCT_CATEGORY) AS PRODUCT_CATEGORIES,
  ROW_NUMBER() OVER (
    PARTITION BY c.REGION
    ORDER BY CASE WHEN COALESCE(o.TOTAL_REVENUE, 0) IS NULL THEN 1 ELSE 0 END DESC, COALESCE(o.TOTAL_REVENUE, 0) DESC
  ) AS REGION_RANK
FROM CRM_CUSTOMERS AS c
LEFT JOIN (
  SELECT
    CUSTOMER_ID AS CUSTOMER_ID,
    COUNT_BIG(*) AS ORDER_COUNT,
    SUM(TOTAL_AMOUNT) AS TOTAL_REVENUE,
    MAX(PRODUCT_CATEGORY) AS PRODUCT_CATEGORY
  FROM ERP_ORDERS
  WHERE
    ORDER_DATE >= ADD_MONTHS(GETDATE(), -12)
  GROUP BY
    CUSTOMER_ID
) AS o
  ON c.CUSTOMER_ID = o.CUSTOMER_ID
LEFT JOIN (
  SELECT
    USER_ID AS CUSTOMER_ID,
    COUNT_BIG(*) AS SESSION_COUNT
  FROM WEB_SESSIONS
  WHERE
    SESSION_DATE >= ADD_MONTHS(GETDATE(), -3)
  GROUP BY
    USER_ID
) AS w
  ON c.CUSTOMER_ID = w.CUSTOMER_ID
WHERE
  c.STATUS <> 'D'
ORDER BY
  CASE WHEN LIFETIME_REVENUE IS NULL THEN 1 ELSE 0 END DESC, LIFETIME_REVENUE DESC